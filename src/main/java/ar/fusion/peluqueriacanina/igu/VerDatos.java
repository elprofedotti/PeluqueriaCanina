    private void cargarTabla() {
        //definir el modelo que queremos que tenga la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            //que filas y columnas no sean editables
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        //establecemos nombres de columnas
        String titulos[]={"Num", "Nombre", "Color", "Raza", "Alergico", "At. Esp.", "Dueño", "Cel."};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //carga de datos desde la BD
        List <Mascotas> listaMascotas = control.traerMascotas();
        
        //recorrer la lista y mostrar cada uno de los elementos en la tabla
        if(listaMascotas!=null){
            for (Mascotas masco : listaMascotas){
                Object[] objeto ={masco.getNumCliente(), masco.getNombre(), masco.getColor(), masco.getRaza(), masco.getAlergico(), masco.getAtencion_especial(), masco.getUnDuenio().getNombre(), masco.getUnDuenio().getCelDuenio()};
                
                modeloTabla.addRow(objeto);
                
            }
        }
        
        tablaMascotas.setModel(modeloTabla);
        
        
        
    }
