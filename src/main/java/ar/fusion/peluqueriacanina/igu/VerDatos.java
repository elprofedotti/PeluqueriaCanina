    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //BOTON ELIMINAR
        // TODO add your handling code here:
        //controlo que la tabla no este vacia
        if (tablaMascotas.getRowCount()>0){
            //controlo que se hay seleccionado una mascota
            if(tablaMascotas.getSelectedRow()!=-1){
                //obtengo ID de mascota a eliminar
                int num_cliente = Integer.parseInt(String.valueOf(tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(),0))); 
                
                //llamo al metodo borrar
                control.borrarMascota(num_cliente);
                
                //aviso al usuario que se borro exitosamente
                mostrarMensaje("Mascota eliminada correctamente", "Info", "Eliminacion de mascota");
                cargarTabla();
            }
            else{
                mostrarMensaje("No selecciono mascota", "Error", "ERROR al eliminar");
            }
        }
        else{
            mostrarMensaje("No hay nada para eliminar en la tabla", "Info", "Sin Mascotas");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

